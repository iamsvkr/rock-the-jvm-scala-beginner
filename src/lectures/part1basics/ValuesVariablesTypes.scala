package lectures.part1basics

object ValuesVariablesTypes extends App {
  // vals are immutable
  // compiler can infer types
  // val => values
  val x: Int = 42
  println(x)

  val aBoolean: Boolean = false;
  val aChar: Char = 'a'

  // var => variables
  // vars are mutable
  var aVar: Int = 4
}
